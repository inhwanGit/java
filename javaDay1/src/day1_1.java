public class day1_1 {
    public static void main(String[] args) {
        //사칙연산이 다 가능
        System.out.println(6+3);
        System.out.println(6-3);
        System.out.println(6*3);
        System.out.println(6/3);

        //-------------------------
        //변수 지정
        int x = 4, y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}