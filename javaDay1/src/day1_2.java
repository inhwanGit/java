public class day1_2 {
    public static void main (String[] args)
    {
        //- 문자 → char
        //- 숫자 → 정수 → byte,short,int,long (서로 크기 차이)
        //
        //            → 실수 → float, double (서로 크기 차이)
        //
        //- 문자열 → String
        //- 논리 → boolean

        char c = 'A';
        byte b = 1;
        short sh = 2;
        int i = 3;
        long l = 4;
        float f = 1.0f;
        double lo = 1.1231231231231231231231;
        String S = "안녕하세요";
        boolean bool = true;

        System.out.println(c);
        System.out.println(b);
        System.out.println(sh);
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
        System.out.println(lo);
        System.out.println(S);
        System.out.println(bool);
    }
}
